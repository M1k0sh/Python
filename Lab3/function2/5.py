def category_imdb(ar):
    all = {}
    n = {}
    for dic in ar:
        for i in dic:
            if i == 'category':
                if dic[i] not in all:
                    all[dic[i]] = dic['imdb']
                    n[dic[i]] = 1
                else:
                    all[dic[i]] += dic['imdb']
                    n[dic[i]] += 1
    for i in all:
        all[i] = all[i] / n[i]
        
    return all
                    
                    
            
